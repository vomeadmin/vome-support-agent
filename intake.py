def run_intake_turn(
    message: str,
    session_context: dict,
    conversation_history: list,
    attachments: list | None = None,
) -> dict:
    """Process one turn of the intake conversation.

    Returns the response dict to send back to Django:
    {
        "reply": str,
        "status": "collecting" | "deflecting" | "confirming" | "complete",
        "kb_article": {...} | None,
        "ticket_created": bool,
        "ticket_id": str | None,
    }
    """
    attachments = attachments or []

    # Step 1: Run Claude to get the conversational reply + structured data
    kb_context = None
    kb_article_response = None

    # Check if previous turn had a kb_query we should search
    # (We look at the last assistant message's JSON block)
    prior_kb_query = _extract_prior_kb_query(conversation_history)
    if prior_kb_query:
        kb_match = _search_kb_combined(
            prior_kb_query,
            session_context.get("locale"),
        )
        if kb_match:
            kb_context = json.dumps(kb_match)
            kb_article_response = {
                "title": kb_match["title"],
                "url": kb_match["url"],
                "days_stale": kb_match["days_stale"],
            }

    # Upfront KB search: run on the raw user message so Claude has
    # KB results available on the very first turn (instead of waiting
    # for Claude to emit a kb_query that only gets searched next turn).
    kb_searched_no_results = False
    if not kb_context and not prior_kb_query and message.strip():
        upfront_match = _search_kb_combined(
            message.strip(),
            session_context.get("locale"),
        )
        if upfront_match:
            if upfront_match["action"] == "flag_stale":
                flag_stale_article(upfront_match)
                kb_searched_no_results = True
            else:
                kb_context = json.dumps(upfront_match)
                kb_article_response = {
                    "title": upfront_match["title"],
                    "url": upfront_match["url"],
                    "days_stale": upfront_match["days_stale"],
                }
        else:
            kb_searched_no_results = True

    messages = _build_intake_messages(
        message=message,
        session_context=session_context,
        conversation_history=conversation_history,
        attachments=attachments,
        kb_context=kb_context,
        kb_searched_no_results=kb_searched_no_results,
    )

    system_prompt = _build_system_prompt(session_context)

    try:
        response = _client.messages.create(
            model="claude-sonnet-4-20250514",
            max_tokens=1024,
            system=system_prompt,
            messages=messages,
        )
        response_text = response.content[0].text
    except Exception as e:
        print(f"[INTAKE] Claude call failed: {e}")
        return {
            "reply": "I'm having trouble processing your request right now. Please try again in a moment.",
            "status": "collecting",
            "kb_article": None,
            "ticket_created": False,
            "ticket_id": None,
        }

    # Step 2: Parse the response
    reply_text, parsed = _parse_intake_response(response_text)
    extracted = parsed.get("extracted", {})
    status = parsed.get("status", "collecting")
    kb_query = parsed.get("kb_query")
    issue_fingerprint = parsed.get("issue_fingerprint")
    auth_check_email = parsed.get("auth_check")

    # Step 2b: Handle auth check if Claude requested one
    if auth_check_email:
        auth_result = _run_auth_check(auth_check_email)
        if auth_result:
            # If bypassable, auto-activate
            if auth_result.get("is_bypassable"):
                bypass_result = _run_auth_bypass(auth_check_email)
                if bypass_result and bypass_result.get("bypassed"):
                    reply_text += (
                        "\n\nI've activated your account. "
                        "You should be able to log in now! "
                        "If you have any trouble, try resetting "
                        "your password here: "
                        "https://www.vomevolunteer.com/forgot"
                    )
                    status = "complete"
            elif auth_result.get("is_active"):
                reply_text += (
                    "\n\nI can see your account is already "
                    "active. Try resetting your password here: "
                    "https://www.vomevolunteer.com/forgot"
                )
            elif not auth_result.get("found"):
                reply_text += (
                    "\n\nI couldn't find an account with that "
                    "email. You may need to register first: "
                    "https://www.vomevolunteer.com/register-volunteer"
                )

    # Step 3: If this is the first turn and Claude generated a kb_query, search now
    if kb_query and not prior_kb_query and not kb_article_response:
        kb_match = _search_kb_combined(
            kb_query,
            session_context.get("locale"),
        )
        if kb_match:
            kb_article_response = {
                "title": kb_match["title"],
                "url": kb_match["url"],
                "days_stale": kb_match["days_stale"],
            }
            # If stale, flag it
            if kb_match["action"] == "flag_stale":
                flag_stale_article(kb_match)
                kb_article_response = None  # Don't show stale articles

    # Step 4: Apply completeness gate
    is_complete, missing = check_completeness(extracted, session_context)

    # Override status based on completeness
    if status == "complete":
        # User confirmed -- create the ticket
        ticket_result = _create_ticket_from_intake(
            extracted=extracted,
            session_context=session_context,
            conversation_history=conversation_history,
            attachments=attachments,
        )

        # Log unmatched issue for KB gap tracking
        if issue_fingerprint and not kb_article_response:
            check_and_create_kb_task(
                fingerprint=issue_fingerprint,
                org_id=session_context.get("org_id"),
                user_email=session_context.get("user_email"),
            )

        # If ticket creation failed, override Claude's optimistic reply
        if not ticket_result.get("ticket_created"):
            print(
                f"[INTAKE] Ticket creation failed for "
                f"{session_context.get('user_email')} — "
                f"overriding reply text"
            )
            reply_text = (
                "I'm sorry, I wasn't able to create your ticket "
                "right now due to a system issue. Your conversation "
                "has been saved — please try again in a few minutes, "
                "or email us at support@vomevolunteer.zohodesk.com "
                "and we'll follow up with you."
            )

        return {
            "reply": reply_text,
            "status": "complete",
            "kb_article": kb_article_response,
            "ticket_created": ticket_result.get("ticket_created", False),
            "ticket_id": ticket_result.get("ticket_id"),
        }

    elif status == "confirming" and not is_complete:
        # Claude thinks we're ready but completeness gate says no.
        # Exception: if we have at least a description, allow it
        # through (frustrated user early exit -- Claude detected
        # frustration and is wrapping up with what it has).
        has_description = bool(
            extracted.get("description")
        )
        if not has_description:
            status = "collecting"

    # Log unmatched issues on each turn where we have a fingerprint
    if issue_fingerprint and not kb_article_response and status not in ("deflecting",):
        check_and_create_kb_task(
            fingerprint=issue_fingerprint,
            org_id=session_context.get("org_id"),
            user_email=session_context.get("user_email"),
        )

    return {
        "reply": reply_text,
        "status": status,
        "kb_article": kb_article_response,
        "ticket_created": False,
        "ticket_id": None,
    }


def _extract_prior_kb_query(conversation_history: list) -> str | None:
    """Extract the kb_query from the last assistant message's JSON block."""
    for turn in reversed(conversation_history):
        if turn.get("role") == "assistant":
            content = turn.get("content", "")
            _, parsed = _parse_intake_response(content)
            return parsed.get("kb_query")
    return None


def _search_kb_combined(
    query: str,
    locale: str | None = None,
) -> dict | None:
    """Search KB using ChromaDB first (semantic), fall back to Zoho API (keyword).

    Returns a dict with {title, url, days_stale, action} or None.
    """
    # Try ChromaDB (nightly-indexed articles)
    try:
        chroma_results = search_kb_articles(
            query, n_results=1, language=locale,
        )
        if chroma_results:
            best = chroma_results[0]
            # Only use if relevance score is reasonable
            if best.get("score") and best["score"] > 0.3:
                days = best.get("days_stale")
                if days is not None and days > 365:
                    action = "flag_stale"
                elif days is not None and days > 90:
                    action = "suggest_with_caveat"
                else:
                    action = "suggest"
                return {
                    "title": best["title"],
                    "url": best["url"],
                    "days_stale": days,
                    "action": action,
                }
    except Exception as e:
        print(f"[KB] ChromaDB search failed, falling back: {e}")

    # Fall back to Zoho keyword search
    return get_best_kb_match(query)
