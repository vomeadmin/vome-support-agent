def _create_ticket_from_intake(
    extracted: dict,
    session_context: dict,
    conversation_history: list,
    attachments: list,
) -> dict:
    """Create a Zoho Desk ticket + ClickUp task from the completed intake.

    Returns {"ticket_created": bool, "ticket_id": str | None}.
    """
    user_email = session_context.get("user_email", "")
    org_name = session_context.get("org_name", "")
    description = extracted.get("description", "No description provided")
    module = extracted.get("module", "other")
    platform = extracted.get("platform", session_context.get("platform", "web"))
    affected_email = extracted.get("affected_user_email", user_email)

    # Generate a clean ticket subject via Claude
    subject = _generate_ticket_subject(
        description, conversation_history,
    )

    # Create Zoho Desk ticket via direct REST API
    final_email = (
        affected_email if affected_email and affected_email != "self"
        else user_email
    )

    # Build a clean HTML body for the ticket (transcript only —
    # metadata is added as an internal comment below)
    ticket_description = _build_ticket_html(
        conversation_history=conversation_history,
        attachments=attachments,
    )

    # Resolve Zoho Desk account + contact so the ticket is
    # properly linked for search and client history.
    account_id = None
    contact_id = None

    if org_name:
        account = zoho_find_or_create_account(org_name)
        if account:
            account_id = str(account.get("id", ""))

    contact = zoho_find_or_create_contact(
        email=final_email,
        first_name=session_context.get("user_first_name", ""),
        last_name=session_context.get("user_last_name", ""),
        account_id=account_id,
    )
    if contact:
        contact_id = str(contact.get("id", ""))

    print(
        f"[INTAKE] Creating Zoho ticket for {final_email} "
        f"(contact={contact_id}, account={account_id}) "
        f"-- subject: {subject[:80]}"
    )
    ticket_data_raw = zoho_create_ticket(
        subject=subject,
        description=ticket_description,
        email=final_email,
        contact_id=contact_id,
        account_id=account_id,
    )

    if not ticket_data_raw or not isinstance(ticket_data_raw, dict):
        print(f"[INTAKE] Failed to create Zoho ticket.")
        return {"ticket_created": False, "ticket_id": None}

    ticket_id = str(ticket_data_raw.get("id", ""))
    ticket_number = str(
        ticket_data_raw.get("ticketNumber", "")
    )

    if not ticket_id:
        print(
            f"[INTAKE] Zoho ticket creation returned no ID. "
            f"Response: {ticket_data_raw}"
        )
        return {"ticket_created": False, "ticket_id": None}

    print(
        f"[INTAKE] Zoho ticket created: "
        f"#{ticket_number} (ID: {ticket_id})"
    )

    # Attach files if any
    # (Attachments are S3 URLs -- Zoho will need them linked, but for now
    #  we include them in the description. Full attachment API integration
    #  can be added later.)

    # CRM enrichment
    crm = fetch_crm_account(user_email)

    # Build ticket_data dict for ClickUp task creation
    ticket_data = {
        "ticket_id": ticket_id,
        "ticket_number": ticket_number,
        "subject": subject,
        "body": description,
        "contact_email": user_email,
        "contact_name": "",  # Not always available from widget
    }

    # Build agent_response string for ClickUp parsing
    # Format it in the way _parse_agent_response expects
    agent_response = (
        f"CLASSIFICATION: Bug\n"
        f"MODULE: {module}\n"
        f"PLATFORM: {platform}\n"
        f"PRIORITY: P3\n"
        f"ISSUE SUMMARY: {description}\n"
        f"SUGGESTED OWNER: Backend\n"
    )

    # Use analysis-based routing for ClickUp
    analysis = {
        "category": "bug",  # Default; could be refined with a classification call
        "complexity": "medium",
        "client_tier": _map_tier_to_score(session_context.get("tier", "")),
        "engineer_type": "backend",
    }

    zoho_url = f"https://desk.zoho.com/agent/vomevolunteer/tickets/{ticket_id}"

    # Note: ClickUp task creation is intentionally NOT triggered here.
    # New widget tickets land in Zoho with status "New" for human triage
    # first. A ClickUp task is only created once a ticket is assigned to
    # the backend or frontend team — handled out-of-band, not from intake.

    # Post Slack ticket brief
    try:
        send_ticket_brief(
            ticket_id=ticket_id,
            ticket_number=ticket_number,
            subject=subject,
            crm=crm,
            agent_response=agent_response,
            clickup_task_url=None,
            zoho_ticket_url=zoho_url,
            clickup_task_id=None,
            contact_email=user_email,
            issue_summary=description,
            new_classification=analysis,
        )
    except Exception as e:
        print(f"[INTAKE] Slack brief failed: {e}")

    # Post internal note on Zoho ticket (direct API)
    try:
        note = (
            f"Ticket created via Support Widget\n\n"
            f"Module: {module}\n"
            f"Platform: {platform}\n"
            f"Affected user: {affected_email}\n"
            f"Org: {org_name} "
            f"({session_context.get('tier', 'unknown')} tier)\n"
            f"Source page: "
            f"{session_context.get('current_page', 'unknown')}\n"
        )
        if attachments:
            note += f"\nAttachments: {', '.join(attachments)}\n"
        zoho_add_comment(ticket_id, note)
    except Exception as e:
        print(f"[INTAKE] Internal note failed: {e}")

    return {"ticket_created": True, "ticket_id": ticket_id}


def _map_tier_to_score(tier: str) -> str:
    """Map plan tier name to the client_tier scoring label."""
    tier_lower = (tier or "").strip().lower()
    tier_map = {
        "ultimate": "very-high",
        "enterprise": "high",
        "pro": "medium",
        "recruit": "low",
    }
    return tier_map.get(tier_lower, "low")


